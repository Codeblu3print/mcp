// ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
export async function handleDocsTool(
  name: string,
  args: any
): Promise<{ content: Array<{ type: string; text: string }>; isError?: boolean }> {
  try {
    switch (name) {
      case 'docs_generate_adr': {
        const { title, context = '' } = args;
        if (!title || title.length > 200) throw new Error('title must be 1-200 characters');
        const result = generateAdrTemplate(context, title);
        return { content: [{ type: 'text', text: result }] };
      }

      case 'docs_generate_readme': {
        const { project_path } = args;
        if (!project_path) throw new Error('project_path is required');
        validatePath(project_path);
        const result = await generateReadmeTemplate(project_path);
        return { content: [{ type: 'text', text: result }] };
      }

      case 'docs_generate_api_doc': {
        const { source_path, max_lines = 500 } = args;
        if (!source_path) throw new Error('source_path is required');
        const safeLines = Math.min(Math.max(1, Number(max_lines) || 500), 2000);
        const result = await generateApiDoc(source_path, safeLines);
        return { content: [{ type: 'text', text: result }] };
      }

      case 'docs_audit_codebase': {
        const { root_path } = args;
        if (!root_path) throw new Error('root_path is required');
        const result = await auditCodebase(root_path);
        return { content: [{ type: 'text', text: result }] };
      }

      default:
        return { content: [{ type: 'text', text: `Unknown docs tool: ${name}` }], isError: true };
    }
  } catch (error: any) {
    return { content: [{ type: 'text', text: `Error: ${error.message}` }], isError: true };
  }
}
