// CODEBASE AUDIT
// ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
async function auditCodebase(rootPath: string): Promise<string> {
  validatePath(rootPath);
  const resolved = path.resolve(rootPath);
  const stats = { totalFiles: 0, totalDirs: 0, languages: {} as Record<string, number> };
  const findings: string[] = [];

  async function scan(dir: string, depth = 0): Promise<void> {
    if (depth > 4) return;
    try {
      const entries = await fs.readdir(dir, { withFileTypes: true });
      for (const entry of entries) {
        if (entry.name === 'node_modules' || entry.name === '.git' || entry.name === 'dist' || entry.name.startsWith('.')) continue;
        const full = path.join(dir, entry.name);
        if (entry.isDirectory()) {
          stats.totalDirs++;
          await scan(full, depth + 1);
        } else {
          stats.totalFiles++;
          const ext = path.extname(entry.name).slice(1) || 'no-ext';
          stats.languages[ext] = (stats.languages[ext] || 0) + 1;
        }
      }
    } catch { /* skip inaccessible */ }
  }

  await scan(resolved);

  // Check for documentation gaps
  const hasReadme = (await fs.readdir(resolved)).some(f => f.toLowerCase() === 'readme.md');
  const hasPkg = (await fs.readdir(resolved)).some(f => f === 'package.json');
  const hasTsConfig = (await fs.readdir(resolved)).some(f => f === 'tsconfig.json');
  const hasGitignore = (await fs.readdir(resolved)).some(f => f === '.gitignore');

  if (!hasReadme) findings.push('- Missing README.md');
  if (!hasPkg) findings.push('- No package.json found (not a Node.js project?)');
  if (!hasTsConfig && hasPkg) findings.push('- No tsconfig.json (TypeScript project without config?)');
  if (!hasGitignore) findings.push('- No .gitignore');
  if (stats.totalFiles > 50 && !hasReadme) findings.push('- Large codebase without README ‚Äî high documentation debt');
  if (!stats.languages['md']) findings.push('- No markdown documentation files found');

  return JSON.stringify({
    summary: {
      totalFiles: stats.totalFiles,
      totalDirectories: stats.totalDirs,
      languages: stats.languages,
      hasReadme,
    },
    documentationStatus: {
      readme: hasReadme ? 'present' : 'MISSING',
      packageJson: hasPkg ? 'present' : 'not found',
      gitignore: hasGitignore ? 'present' : 'MISSING',
      tsConfig: hasTsConfig ? 'present' : 'not applicable',
    },
    recommendations: findings.length
      ? findings
      : ['- Codebase appears well organized. Consider adding architecture documentation (ADRs) for key decisions.'],
    generatedBy: 'CodeBlueprint mcp-docs-generator v0.1.0',
  }, null, 2);
}

