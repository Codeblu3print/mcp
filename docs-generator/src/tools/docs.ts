// API DOC TEMPLATE
// ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
async function generateApiDoc(sourcePath: string, maxLines = 500): Promise<string> {
  validatePath(sourcePath);
  const resolved = path.resolve(sourcePath);
  let content = '';
  try {
    content = await fs.readFile(resolved, 'utf-8');
    const lines = content.split('\n').slice(0, maxLines);
    content = lines.join('\n');
  } catch (e: any) {
    throw new Error(`Cannot read file: ${e.message}`);
  }

  const filename = path.basename(resolved);
  const functions: string[] = [];
  const classes: string[] = [];
  const routes: string[] = [];

  // Extract function declarations (JS/TS)
  for (const line of content.split('\n')) {
    const fn = line.match(/^\s*(?:export\s+)?(?:async\s+)?function\s+(\w+)/);
    if (fn) functions.push(fn[1]);
    const cls = line.match(/^\s*(?:export\s+)?class\s+(\w+)/);
    if (cls) classes.push(cls[1]);
    const route = line.match(/[@](?:get|post|put|patch|delete)\s+['"]?(\/[^\s'"()]+)/);
    if (route) routes.push(route[1]);
  }

  return `# API Documentation: ${filename}

**File:** \`${sourcePath}\`  
**Generated:** ${new Date().toISOString().split('T')[0]}

## Overview

${classes.length ? `**Classes:** ${classes.join(', ')}` : ''}
${functions.length ? `**Functions:** ${functions.length} exported functions detected` : ''}
${routes.length ? `**Routes:** ${routes.join(', ')}` : ''}

## Code

\`\`\`typescript
${content}
\`\`\`

## Endpoints

${routes.length ? routes.map((r, i) => `${i + 1}. \`${r}\``).join('\n') : '_No HTTP routes detected in this file._'}

## Usage Example

\`\`\`typescript
// Import the module
import { ${classes[0] || 'module'} } from '${filename.replace(/\.[^.]+$/, '')}';

// ${functions[0] ? `Call a function: ${functions[0]}()` : 'Use the exported API'}
\`\`\`

---
*Generated by CodeBlueprint MCP Docs Generator*
`;
}

